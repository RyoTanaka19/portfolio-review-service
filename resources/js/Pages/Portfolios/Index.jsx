import React, { useState } from "react";
import { Inertia } from "@inertiajs/inertia";
import { InertiaLink } from "@inertiajs/inertia-react";
import AppLayout from "@/Layouts/AppLayout";
import axios from "axios";

export default function Index({
    portfolios,
    auth,
    filters = {},
    allTags = [],
}) {
    const [userNameFilter, setUserNameFilter] = useState(
        filters.user_name || ""
    );
    const [tagFilter, setTagFilter] = useState(filters.tag || "");
    const [suggestions, setSuggestions] = useState([]); // „Ç™„Éº„Éà„Ç≥„É≥„Éó„É™„Éº„ÉàÂÄôË£ú

    // „Éñ„ÉÉ„ÇØ„Éû„Éº„ÇØÁä∂ÊÖã„Çí‰øùÊåÅ
    const initialBookmarks = {};
    portfolios.forEach((p) => {
        initialBookmarks[p.id] = p.is_bookmarked || false;
    });
    const [bookmarks, setBookmarks] = useState(initialBookmarks);

    // ÂâäÈô§Âá¶ÁêÜ
    const handleDelete = (id) => {
        if (confirm("Êú¨ÂΩì„Å´ÂâäÈô§„Åó„Åæ„Åô„ÅãÔºü")) {
            Inertia.delete(`/portfolio/${id}`);
        }
    };

    // Ê§úÁ¥¢Âá¶ÁêÜ
    const handleSearch = () => {
        Inertia.get(
            route("dashboard"),
            { user_name: userNameFilter, tag: tagFilter },
            { preserveState: true }
        );
        setSuggestions([]); // Ê§úÁ¥¢ÊôÇ„ÅØÂÄôË£ú„ÇíÊ∂à„Åô
    };

    // „Éñ„ÉÉ„ÇØ„Éû„Éº„ÇØ„ÅÆÁôªÈå≤„ÉªËß£Èô§
    const toggleBookmark = (portfolioId) => {
        const isBookmarked = bookmarks[portfolioId];

        if (isBookmarked) {
            Inertia.delete(route("bookmark.destroy", portfolioId), {
                onSuccess: () => {
                    setBookmarks((prev) => ({ ...prev, [portfolioId]: false }));
                },
            });
        } else {
            Inertia.post(
                route("bookmark.store", portfolioId),
                {},
                {
                    onSuccess: () => {
                        setBookmarks((prev) => ({
                            ...prev,
                            [portfolioId]: true,
                        }));
                    },
                }
            );
        }
    };

    // üîç „É¶„Éº„Ç∂„ÉºÂÄôË£úÂèñÂæó
    const fetchUserSuggestions = async (query) => {
        if (!query) {
            setSuggestions([]);
            return;
        }

        try {
            const res = await axios.get("/autocomplete/users", {
                params: { query },
            });
            setSuggestions(res.data);
        } catch (err) {
            console.error("„É¶„Éº„Ç∂„ÉºÂÄôË£úÂèñÂæó„Ç®„É©„Éº:", err);
            setSuggestions([]);
        }
    };

    // ÂÖ•ÂäõÊôÇ„Å´„Çµ„Ç∏„Çß„Çπ„ÉàÂèñÂæó
    const handleUserInput = (e) => {
        const value = e.target.value;
        setUserNameFilter(value);
        fetchUserSuggestions(value);
    };

    return (
        <AppLayout>
            <header className="px-8 py-6 bg-white shadow flex flex-col items-center">
                <h1 className="text-2xl font-bold mb-4">ÊäïÁ®ø‰∏ÄË¶ß</h1>

                <div className="w-full max-w-3xl">
                    <div className="grid grid-cols-1 md:grid-cols-3 gap-4 items-end">
                        {/* „Çø„Ç∞Ê§úÁ¥¢ÔºàÂ∑¶Ôºâ */}
                        <div className="flex flex-col">
                            <label className="text-sm font-medium mb-1">
                                „Çø„Ç∞„ÅßÊ§úÁ¥¢
                            </label>
                            <select
                                value={tagFilter}
                                onChange={(e) => setTagFilter(e.target.value)}
                                className="border px-3 py-2 rounded focus:outline-none focus:ring-2 focus:ring-blue-400"
                            >
                                <option value="">„Çø„Ç∞„ÇíÈÅ∏Êäû</option>
                                {allTags.map((tag, idx) => (
                                    <option key={idx} value={tag}>
                                        {tag}
                                    </option>
                                ))}
                            </select>
                        </div>

                        {/* „É¶„Éº„Ç∂„ÉºÂêçÊ§úÁ¥¢Ôºà‰∏≠Â§ÆÔºâ */}
                        <div className="flex flex-col relative">
                            <label className="text-sm font-medium mb-1">
                                „É¶„Éº„Ç∂„ÉºÂêç„ÅßÊ§úÁ¥¢
                            </label>
                            <input
                                type="text"
                                placeholder="‰æã: Tanaka"
                                value={userNameFilter}
                                onChange={handleUserInput}
                                className="border px-3 py-2 rounded focus:outline-none focus:ring-2 focus:ring-blue-400"
                            />
                            {/* ÂÄôË£ú„É™„Çπ„Éà */}
                            {suggestions.length > 0 && (
                                <ul className="absolute top-full mt-2 z-10 bg-white border w-full max-h-48 overflow-y-auto shadow rounded">
                                    {suggestions.map((user) => (
                                        <li
                                            key={user.id}
                                            className="px-3 py-2 hover:bg-gray-100 cursor-pointer"
                                            onClick={() => {
                                                setUserNameFilter(user.name);
                                                setSuggestions([]);
                                            }}
                                        >
                                            {user.name}
                                        </li>
                                    ))}
                                </ul>
                            )}
                        </div>

                        {/* Ê§úÁ¥¢„Éú„Çø„É≥ÔºàÂè≥Ôºâ */}
                        <div className="flex items-center mt-2 md:mt-0">
                            <button
                                type="button"
                                onClick={handleSearch}
                                className="w-full md:w-auto px-6 py-2 bg-blue-500 text-white rounded hover:bg-blue-600 transition"
                            >
                                Ê§úÁ¥¢
                            </button>
                        </div>
                    </div>
                </div>
            </header>

            {/* ‰ª•‰∏ã„ÅØ„Éù„Éº„Éà„Éï„Ç©„É™„Ç™‰∏ÄË¶ßÈÉ®ÂàÜÔºàÁúÅÁï•„Åõ„ÅöÊÆã„Åó„Å¶OKÔºâ */}
            <main className="px-8 py-6 grid grid-cols-1 md:grid-cols-2 lg:grid-cols-3 gap-4">
                {portfolios.map((p) => {
                    const averageRating =
                        p.reviews && p.reviews.length > 0
                            ? (
                                  p.reviews.reduce(
                                      (sum, r) => sum + r.rating,
                                      0
                                  ) / p.reviews.length
                              ).toFixed(1)
                            : null;

                    return (
                        <div
                            key={p.id}
                            className="bg-white p-4 rounded shadow hover:shadow-md transition duration-200"
                        >
                            <div className="flex justify-between items-center mb-2">
                                <h2 className="font-bold text-lg truncate">
                                    <InertiaLink
                                        href={`/portfolio/${p.id}`}
                                        className="text-blue-500 hover:underline"
                                    >
                                        {p.title.length > 30
                                            ? p.title.slice(0, 30) + "‚Ä¶"
                                            : p.title}
                                    </InertiaLink>
                                </h2>
                                <span className="text-sm text-gray-500">
                                    {p.user_name}
                                </span>
                            </div>

                            {p.image_url && (
                                <img
                                    src={p.image_url}
                                    alt={p.title}
                                    className="w-full h-40 object-cover mb-2 rounded"
                                />
                            )}

                            <p className="text-gray-700 text-sm mb-2 line-clamp-3">
                                {p.description}
                            </p>

                            {averageRating ? (
                                <p className="text-yellow-600 font-semibold mb-2">
                                    Âπ≥ÂùáË©ï‰æ°: {averageRating} / 5 (
                                    {p.reviews.length}‰ª∂)
                                </p>
                            ) : (
                                <p className="text-gray-500 mb-2">
                                    „É¨„Éì„É•„Éº„ÅØ„Åæ„Å†„ÅÇ„Çä„Åæ„Åõ„Çì
                                </p>
                            )}

                            {p.tags && p.tags.length > 0 && (
                                <div className="mt-2 flex flex-wrap gap-1">
                                    {p.tags.map((tag, idx) => (
                                        <button
                                            key={idx}
                                            type="button"
                                            onClick={() => {
                                                setTagFilter(tag);
                                                setUserNameFilter("");
                                                handleSearch();
                                            }}
                                            className="bg-gray-200 text-gray-800 px-2 py-1 rounded text-xs"
                                        >
                                            {tag}
                                        </button>
                                    ))}
                                </div>
                            )}

                            {auth?.user && (
                                <button
                                    type="button"
                                    onClick={() => toggleBookmark(p.id)}
                                    className={`mt-2 px-2 py-1 rounded text-sm ${
                                        bookmarks[p.id]
                                            ? "bg-yellow-400 text-white"
                                            : "bg-gray-200 text-gray-800"
                                    }`}
                                >
                                    {bookmarks[p.id]
                                        ? "‚òÖ „ÅäÊ∞ó„Å´ÂÖ•„Çä"
                                        : "‚òÜ „ÅäÊ∞ó„Å´ÂÖ•„Çä"}
                                </button>
                            )}

                            {p.url && (
                                <a
                                    href={p.url}
                                    target="_blank"
                                    rel="noopener noreferrer"
                                    className="text-blue-500 text-sm block mt-2"
                                >
                                    „Çµ„Ç§„Éà„ÇíË¶ã„Çã‚Üí
                                </a>
                            )}

                            {auth?.user?.id === p.user_id && (
                                <div className="mt-4 flex justify-end space-x-2">
                                    <InertiaLink
                                        href={`/portfolio/${p.id}/edit`}
                                        className="bg-yellow-500 text-white px-3 py-1 rounded hover:bg-yellow-600 text-sm"
                                    >
                                        Á∑®ÈõÜ
                                    </InertiaLink>
                                    <button
                                        type="button"
                                        onClick={() => handleDelete(p.id)}
                                        className="bg-red-500 text-white px-3 py-1 rounded hover:bg-red-600 text-sm"
                                    >
                                        ÂâäÈô§
                                    </button>
                                </div>
                            )}
                        </div>
                    );
                })}
            </main>
        </AppLayout>
    );
}
